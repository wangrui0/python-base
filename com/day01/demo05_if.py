# if 的使用
age = 19
if age > 18:
    print("if年龄为：%d,已经成年" % age)
    print("if庆祝一下")
print("if结束了")
# if else 的使用
if age > 18:
    print("年龄为：%d,已经成年" % age)
    print("庆祝一下")
    print("不错呀")
else:
    print("年龄为：%d,没有成年" % age)
    print("还很小呀")
print("IF ELSE 结束了")

